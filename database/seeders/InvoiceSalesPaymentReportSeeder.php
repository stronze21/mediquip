    private const INVOICE_PREFIX = 'RPT-';

    public function run(): void
    {
        DB::transaction(function () {
            Sale::query()
                ->where('invoice_number', 'like', self::INVOICE_PREFIX . '%')
                ->delete();

            $warehouse = $this->warehouse();
            $users = $this->users();
            $customers = $this->customers();
            $products = $this->products($warehouse);
            $services = $this->services();

            foreach ($this->invoiceFixtures() as $index => $fixture) {
                $completedAt = Carbon::parse($fixture['completed_at']);
                $items = $this->resolveItems($fixture['items'], $products, $services);
                $subtotal = collect($items)->sum(fn ($item) => $item['unit_price'] * $item['quantity']);
                $discount = (float) ($fixture['discount_amount'] ?? 0);
                $tax = round(max($subtotal - $discount, 0) * ((float) ($fixture['tax_rate'] ?? 0) / 100), 2);
                $total = round($subtotal - $discount + $tax, 2);
                $paid = round((float) $fixture['paid_amount'], 2);

                $sale = Sale::create([
                    'invoice_number' => self::INVOICE_PREFIX . $completedAt->format('Ymd') . '-' . str_pad((string) ($index + 1), 4, '0', STR_PAD_LEFT),
                    'invoice_type' => $fixture['invoice_type'],
                    'customer_id' => $customers[$fixture['customer']]->id,
                    'warehouse_id' => $warehouse->id,
                    'user_id' => $users[$fixture['user']]->id,
                    'subtotal' => $subtotal,
                    'discount_amount' => $discount,
                    'tax_type' => $fixture['tax_type'] ?? 'exclusive',
                    'tax_rate' => $fixture['tax_rate'] ?? 0,
                    'tax_amount' => $tax,
                    'total_amount' => $total,
                    'paid_amount' => min($paid, $total),
                    'change_amount' => max($paid - $total, 0),
                    'payment_method' => $fixture['payment_method'],
                    'payment_terms' => $fixture['payment_terms'] ?? null,
                    'due_date' => $fixture['due_date'] ?? null,
                    'payment_status' => $fixture['payment_status'],
                    'status' => 'completed',
                    'notes' => 'Generated by InvoiceSalesPaymentReportSeeder for report testing.',
                    'completed_at' => $completedAt,
                    'created_at' => $completedAt,
                    'updated_at' => $completedAt,
                ]);

                foreach ($items as $item) {
                    SaleItem::create([
                        'sale_id' => $sale->id,
                        'product_id' => $item['product_id'],
                        'service_id' => $item['service_id'],
                        'item_type' => $item['item_type'],
                        'product_name' => $item['name'],
                        'product_sku' => $item['sku'],
                        'quantity' => $item['quantity'],
                        'unit_price' => $item['unit_price'],
                        'discount_amount' => 0,
                        'total_price' => $item['unit_price'] * $item['quantity'],
                        'cost_price' => $item['cost_price'],
                        'serial_numbers' => null,
                        'created_at' => $completedAt,
                        'updated_at' => $completedAt,
                    ]);
                }
            }
        });
    }


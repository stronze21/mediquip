    private function resolveItems(array $fixtureItems, array $products, array $services): array
    {
        return collect($fixtureItems)
            ->map(function ($item) use ($products, $services) {
                if ($item['type'] === 'service' && isset($services[$item['key']])) {
                    $service = $services[$item['key']];

                    return [
                        'product_id' => null,
                        'service_id' => $service->id,
                        'item_type' => 'service',
                        'name' => $service->name,
                        'sku' => $service->code,
                        'quantity' => $item['quantity'],
                        'unit_price' => (float) $service->price,
                        'cost_price' => 0,
                    ];
                }

                $product = $products[$item['key']];

                return [
                    'product_id' => $product->id,
                    'service_id' => null,
                    'item_type' => 'product',
                    'name' => $product->name,
                    'sku' => $product->sku,
                    'quantity' => $item['quantity'],
                    'unit_price' => (float) $product->selling_price,
                    'cost_price' => (float) $product->cost_price,
                ];
            })
            ->all();
    }
